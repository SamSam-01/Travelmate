CREATE POLICY "Users can view planned outings they participate in" 
    ON planned_outings FOR SELECT 
    USING (
        creator_id = auth.uid() OR
        EXISTS (
            SELECT 1 FROM planned_outing_participants
            WHERE planned_outing_participants.planned_outing_id = planned_outings.id
            AND planned_outing_participants.profile_id = auth.uid()
        )
    );

DROP POLICY IF EXISTS "Users can create planned outings" ON planned_outings;
CREATE POLICY "Users can create planned outings" 
