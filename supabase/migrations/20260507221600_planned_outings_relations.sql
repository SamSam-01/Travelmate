-- Table: planned_outings
CREATE TABLE IF NOT EXISTS planned_outings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Add creator_id so the creator can fetch the row immediately after inserting it (bypassing the catch-22)
ALTER TABLE planned_outings ADD COLUMN IF NOT EXISTS creator_id UUID DEFAULT auth.uid();

-- Table: planned_outing_participants
CREATE TABLE IF NOT EXISTS planned_outing_participants (
    planned_outing_id BIGINT NOT NULL REFERENCES planned_outings(id) ON DELETE CASCADE,
    profile_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
    PRIMARY KEY (planned_outing_id, profile_id)
);

-- Table: planned_outing_activities
CREATE TABLE IF NOT EXISTS planned_outing_activities (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    planned_outing_id BIGINT NOT NULL REFERENCES planned_outings(id) ON DELETE CASCADE,
    activity_id UUID NOT NULL REFERENCES activities(id) ON DELETE CASCADE,
    time TEXT NOT NULL DEFAULT '',
    sort_order INTEGER NOT NULL DEFAULT 0
);

